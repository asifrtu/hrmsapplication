<h1>LEAVE POLICY</h1>

<h6>Purpose</h6>
<p>The objective of this policy is to explain the leave entitlement and procedure for availing
leave. You are eligible to the following leave benefits.</p>
<br>

<h6>Annual / Earned Leave (EL)</h6>
<p>Our annual vacation plan is designed to provide you with the opportunity to rest and get away
from the everyday routine.</p>
<p>1 EL will be added to your kitty in every month so you will be entitled to annual leave of up
to 12 working days in a calendar year. The EL will be accrued only after the completion of
each year. You shall be entitled to earned leave only on confirmation of services which is 90
days.</p>
<p>Unavailed earned leaves if any for the year can be carried over to the next year. However you
can carry over a maximum of 8 ELs only. An employee, once entitled for Earned leave, is
advised to avail at least minimum of 5 days leave per annum. In order to satisfy your
preference and to meet the staffing needs of your department, you are advised to discuss your
vacation plans at least two weeks in advance with your reporting manager.</p>
<br>
<h6>Casual / Sick Leave (CL)</h6>
<p>You are entitled to casual/Sick leave of up to 12 working days in a calendar year. This leave
is for an employee to attend to his/her personal tasks, etc. The leave has to be applied and
approved at least 2 days in advance.  </p>

<p>Not more than 2 consecutive days of leave will be approved in the case of CL. This leave has
to be consumed in the same year, else is lapsed on 31 st  December every year.</p>
<br>
<h6>Maternity Leave</h6>
<p>Female employees who have been working with the company for not less than 90 days will
be eligible for 90 days of paid maternity leave, up to two confinements. To facilitate
arrangements for cover of duties, application for maternity leave will have to be made not
less than two months prior to the date of commencement of leave.  </p>
<p>Absence from work due to miscarriage or any other illness arising out of pregnancy will not
be considered as maternity leave, but as normal medical leave. Employees covered under
ESI, shall govern as per the ESI guidelines.</p>

<br>
<h6>Paternity Leave</h6>
<p>Male Employees will be granted paternity leave for 2 working days. The leave should be
availed within three weeks time of the birth of the child. This will be applicable up to a
maximum of two confinements.</p>
<br>
<h6>Compensatory Offs</h6>
<p>An employee who has worked on a holiday or came on special request is eligible for a
compensatory off. However, this has to be approved by the Reporting Manager with
appropriate justification for such extra work and efforts.</p>

<br>
<h6>Holidays</h6>
<p>Holidays commemorating events of national, social and religious significance are offered to
employees. Apart from scheduled weekend/ earned &amp; casual leaves there are 10days declared
as paid holidays every year.</p>
<p>Holidays list will be put up every year in January and accordingly applicable. </p>
<br>
<h6>Late comings:-</h6>
<p>If anybody report to office after 10:30 AM then it will be marked as late coming and it can
cause deduction from the salary. So please avoid late comings. Exceptions will always be
taken care of.</p>

